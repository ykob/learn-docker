CREATE TABLE person (
  id BIGINT NOT NULL AUTO_INCREMENT,
  name VARCHAR(60) NOT NULL,
  created_at DATETIME(6) NOT NULL DEFAULT NOW(6),
  updated_at DATETIME(6) NOT NULL DEFAULT NOW(6) ON UPDATE NOW(6),
  PRIMARY KEY (id)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4;

CREATE TABLE time (
  start DATETIME(6),
  end DATETIME(6),
  current DATETIME(6)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4;
